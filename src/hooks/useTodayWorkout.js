const useTodayWorkout = () => {
  const startedDate = new Date("2021-07-13");

  const whichWorkoutShoudIDo = (startedDate, schedule) => {
    const date = new Date();
    const todayLesson =
      (Math.floor((date - startedDate) / (60 * 60 * 24 * 1000)) + 1) % 16;
    const index = schedule.findIndex((lesson) => lesson.day === todayLesson);
    return schedule[index];
  };

  const workout = whichWorkoutShoudIDo(startedDate, chloeTingTwoWeeksSchedule);

  return [workout];
};

export default useTodayWorkout;
